# Object-Oriented Programming (OOP) is a programming paradigm that revolves around the concept of objects, which are instances of classes.
#  In OOP, programs are designed by modeling real-world entities as objects and defining their behavior through methods and their attributes 
#  through properties. OOP provides a structured and modular approach to writing code, making it easier to understand, maintain, and extend.

# The four pillars of OOP are the fundamental principles that guide the design and implementation of object-oriented systems. 
#  These pillars are:

# Encapsulation:
# Encapsulation refers to the bundling of data (attributes) and methods that operate on that data within a single unit called a class.
#  The internal state and behavior of the object are hidden from the outside world, and access to this internal state is controlled through
#  public interfaces (public methods). This principle promotes data hiding, which enhances the security and robustness of the codebase, as well
#  as the ability to change the implementation details without affecting other parts of the program.

# Abstraction:
# Abstraction involves representing the essential features of an object while hiding the unnecessary details. In other words, 
# it focuses on what an object does rather than how it does it. Abstract classes and interfaces are used to define common behaviors 
# and properties that multiple objects can share. This allows programmers to work at a higher level of abstraction, leading to more modular
# and maintainable code.

# Inheritance:
# Inheritance is a mechanism that allows one class (called the subclass or derived class) to inherit the properties and behaviors of 
#  another class (called the superclass or base class). This promotes code reuse, as the subclass can reuse the code of the superclass
#  without having to duplicate it. It enables the creation of hierarchical relationships between classes, where the subclasses can
#  specialize or extend the behavior of the superclass.

# Polymorphism:
# Polymorphism allows objects of different classes to be treated as objects of a common superclass. This means that a single method 
# or function can work with objects of multiple types, as long as they are related through inheritance or implement a common interface.
# Polymorphism enables dynamic binding, where the appropriate method to be called is determined at runtime based on the actual type of the object.
# This flexibility and adaptability make code more versatile and extensible.

# By adhering to these four pillars of OOP, programmers can create well-structured, flexible, and maintainable software systems that model
# the real-world entities efficiently, promoting reusability and readability in the process.